package com.viettel.api.vm;

/**
 *
 * @author TungLT - ITSOL
 *
 */
public class CommonVM {

	private String unitId;

	private String name;

	private String lstArea;

	private String lstProvince;

	public String getUnitId() {
		return unitId;
	}

	public void setUnitId(String unitId) {
		this.unitId = unitId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLstArea() {
		return lstArea;
	}

	public void setLstArea(String lstArea) {
		this.lstArea = lstArea;
	}

	public String getLstProvince() {
		return lstProvince;
	}

	public void setLstProvince(String lstProvince) {
		this.lstProvince = lstProvince;
	}

}
