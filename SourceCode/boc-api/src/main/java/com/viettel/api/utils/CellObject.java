public class CellObject {
    private int row;
    private int cell;
    private String value;
    public int getRow() {
        return row;
    }
    public void setRow(int row) {
        this.row = row;
    }
    public int getCell() {
        return cell;
    }
    public void setCell(int cell) {
        this.cell = cell;
    }
    public String getValue() {
        return value;
    }
    public void setValue(String value) {
        this.value = value;
    }
}
